public class getComplaintInformation {
    public String ComplaintType;
    public  String ComplaintDetails;
    public String ComplaintLocation;
    public  String Complaint;
    public String ComplaintDate;
    public String ComplaintTime;

    public String getComplaintType() {
        return ComplaintType;
    }

    public void setComplaintType(String complaintType) {
        ComplaintType = complaintType;
    }

    public String getComplaintDetails() {
        return ComplaintDetails;
    }

    public void setComplaintDetails(String complaintDetails) {
        ComplaintDetails = complaintDetails;
    }

    public String getComplaintLocation() {
        return ComplaintLocation;
    }

    public void setComplaintLocation(String complaintLocation) {
        ComplaintLocation = complaintLocation;
    }

    public String getComplaint() {
        return Complaint;
    }

    public void setComplaint(String complaint) {
        Complaint = complaint;
    }

    public String getComplaintDate() {
        return ComplaintDate;
    }

    public void setComplaintDate(String complaintDate) {
        ComplaintDate = complaintDate;
    }

    public String getComplaintTime() {
        return ComplaintTime;
    }

    public void setComplaintTime(String complaintTime) {
        ComplaintTime = complaintTime;
    }
}



